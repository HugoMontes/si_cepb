	<p>La Confederación de Empresarios Privados de Bolivia, con el apoyo de la ACT/EMP de la OIT, ha desarrollado un nuevo servicio; denominado “Sistema de Información Económica (SIE)” que ofrece a sus gremios regionales y sectoriales y, a través de ellos a las empresas; información económica de fuentes oficiales, organizada de una manera amigable, dinámica y rápida, bajo la premisa de que la información constituye uno de los principales insumos para la toma de decisiones; que permite prevenir futuros contingentes y principalmente establecer medidas adecuadas ante situaciones adversas o aprovechar competitivamente las coyunturas positivas.</p>

